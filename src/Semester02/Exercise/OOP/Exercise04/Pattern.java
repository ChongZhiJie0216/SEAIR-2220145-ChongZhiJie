package Semester02.Exercise.Exercise04;

public class Pattern {
    public void Display(){
        for (int i = 0; i < 10; i++) {
            System.out.println("*");
        }
    }
    public void Display(char symbol) {
        for (int i = 0; i < 10; i++) {
            System.out.println(symbol);
        }
    }
}
